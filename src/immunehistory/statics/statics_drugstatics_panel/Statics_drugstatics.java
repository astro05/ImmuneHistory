package immunehistory.statics.statics_drugstatics_panel;

/**
 *
 * @author joyultimates
 */
public class Statics_drugstatics {
    
    private String DrugName;
    private String age1;
    private String age2;
    private String age3;

    public Statics_drugstatics(String DrugName, String age1, String age2, String age3) {
        this.DrugName = DrugName;
        this.age1 = age1;
        this.age2 = age2;
        this.age3 = age3;
    }

    /**
     * @return the DrugName
     */
    public String getDrugName() {
        return DrugName;
    }

    /**
     * @param DrugName the DrugName to set
     */
    public void setDrugName(String DrugName) {
        this.DrugName = DrugName;
    }

    /**
     * @return the age1
     */
    public String getAge1() {
        return age1;
    }

    /**
     * @param age1 the age1 to set
     */
    public void setAge1(String age1) {
        this.age1 = age1;
    }

    /**
     * @return the age2
     */
    public String getAge2() {
        return age2;
    }

    /**
     * @param age2 the age2 to set
     */
    public void setAge2(String age2) {
        this.age2 = age2;
    }

    /**
     * @return the age3
     */
    public String getAge3() {
        return age3;
    }

    /**
     * @param age3 the age3 to set
     */
    public void setAge3(String age3) {
        this.age3 = age3;
    }
    
    
    
    
}
